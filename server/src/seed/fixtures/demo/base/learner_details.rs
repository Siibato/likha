pub fn demo_learner_details() -> Vec<LearnerDetailsSpec> {
    LEARNER_DATA
        .iter()
        .map(|d| LearnerDetailsSpec {
            id: lid(d.username),
            user_id: uid(d.username),
            lrn: Some(d.lrn.into()),
            sex: Some(d.sex.into()),
            track_strand: Some(d.track.into()),
            curriculum: Some(d.curriculum.into()),
            birthdate: NaiveDate::parse_from_str(d.birthdate, "%Y-%m-%d").ok(),
            birthplace: Some(d.birthplace.into()),
            home_address: Some(d.home_address.into()),
            father_name: Some(d.father_name.into()),
            father_contact: Some(d.father_contact.into()),
            mother_name: Some(d.mother_name.into()),
            mother_contact: Some(d.mother_contact.into()),
            guardian_name: Some(d.guardian_name.into()),
            guardian_contact: Some(d.guardian_contact.into()),
            date_admitted: NaiveDate::parse_from_str(d.date_admitted, "%Y-%m-%d").ok(),
        })
        .collect()
}
